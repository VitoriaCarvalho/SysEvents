    public TelaMeuPerfil() {
        initComponents();
        try{
            InetAddress iAddress = InetAddress.getByName("localhost");
            socket = new Socket(iAddress, 12345);
            OutputStream os = socket.getOutputStream();
            OutputStreamWriter osw = new OutputStreamWriter(os);
            BufferedWriter bw = new BufferedWriter(osw);
            bw.write("BuscarMeuPerfil>" + TelaLogin.campoEmail + "\n");
            bw.flush();

            InputStream is = socket.getInputStream();
            InputStreamReader isr = new InputStreamReader(is);
            BufferedReader br = new BufferedReader(isr);
            String result = br.readLine(); //Retorna: nome,cpf,dataNasc,email,endereco
            LabelCpfPerfil.setText("");
            labelDataNascPerfil.setText("");
            labelEmailPerfil.setText("");
            labelEnderecoPerfil.setText("");
            labelNomePerfil.setText("");
            if(!result.equals("@")) {
                String[] dados = result.split(",");
                LabelCpfPerfil.setText(dados[1]);
                labelDataNascPerfil.setText(dados[2]);
                labelEmailPerfil.setText(dados[3]);
                labelEnderecoPerfil.setText(dados[4]);
                labelNomePerfil.setText(dados[0]);
                campoCpf = LabelCpfPerfil.getText();
                result = null;
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao exibir perfil!");
                TelaHomeUsuario hu = new TelaHomeUsuario();
                hu.setVisible(true);
                this.setVisible(false);
            }
        } catch (Exception ex){
            System.out.println("HelloClient exception: "+ex.getMessage());
            ex.printStackTrace();
        }
    }

